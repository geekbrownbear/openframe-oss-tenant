#!/usr/bin/env node

/**
 * Reads schema.graphql, extracts every `enum` definition and emits a single
 * TypeScript module in Prisma-style — the value (const object) and the type
 * share the same name, so consumers can use either:
 *
 *   export const SubscriptionStatus = { ACTIVE: 'ACTIVE', ... } as const;
 *   export type SubscriptionStatus = (typeof SubscriptionStatus)[keyof typeof SubscriptionStatus];
 *
 *   // usage
 *   SubscriptionStatus.ACTIVE        // value
 *   const x: SubscriptionStatus      // type
 *
 * The output lives in `src/generated/schema-enums.ts` (gitignored). It is kept
 * OUT of `src/__generated__/` on purpose — relay-compiler owns that directory
 * and prunes any file there that isn't a Relay artifact.
 * Run via `npm run generate-enums`; chained into `npm run build`.
 */

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { parse } from 'graphql';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SCHEMA_PATH = resolve(ROOT, 'schema.graphql');
const OUT_PATH = resolve(ROOT, 'src/generated/schema-enums.ts');

const sdl = readFileSync(SCHEMA_PATH, 'utf-8');
const doc = parse(sdl);

const enums = doc.definitions
  .filter(d => d.kind === 'EnumTypeDefinition')
  .sort((a, b) => a.name.value.localeCompare(b.name.value));

const lines = ['// @generated by scripts/generate-schema-enums.mjs from schema.graphql — DO NOT EDIT', ''];

for (const def of enums) {
  const name = def.name.value;
  const values = (def.values ?? []).map(v => v.name.value);
  lines.push(`export const ${name} = {`);
  for (const v of values) {
    lines.push(`  ${v}: '${v}',`);
  }
  lines.push(`} as const;`);
  lines.push(`export type ${name} = (typeof ${name})[keyof typeof ${name}];`);
  lines.push('');
}

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, lines.join('\n'));
console.log(`Generated ${enums.length} enum(s) → ${OUT_PATH.replace(`${ROOT}/`, '')}`);
